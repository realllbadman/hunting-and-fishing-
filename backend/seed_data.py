# Upsert logic — keeps the DB in sync with PRODUCTS_SEED on startup.
# ════════════════════════════════════════════════════════════════════════
def sync_products(db):
    """Insert new, update changed, and remove products no longer in the list."""
    seed_slugs = {p["slug"] for p in PRODUCTS_SEED}
    if seed_slugs:
        db.query(Product).filter(~Product.slug.in_(seed_slugs)).delete(
            synchronize_session=False
        )
    existing = {p.slug: p for p in db.query(Product).all()}
    for entry in PRODUCTS_SEED:
        data = _normalize(entry)
        row = existing.get(data["slug"])
        if row is None:
            db.add(Product(**data))
        else:
            for key, value in data.items():
                setattr(row, key, value)
    db.commit()


def _normalize(entry: dict) -> dict:
    """Coerce a product dict into column-ready values."""
    features = entry.get("features")
    if isinstance(features, (list, tuple)):
        features = json.dumps(list(features))
    return {
        "slug":           entry["slug"],
        "name":           entry["name"],
        "brand":          entry["brand"],
        "category":       entry["category"],
        "subcategory":    entry.get("subcategory"),
        "model_number":   entry.get("model_number"),
        "price":          float(entry["price"]),
        "price_max":      (float(entry["price_max"])
                           if entry.get("price_max") is not None else None),
        "original_price": (float(entry["original_price"])
                           if entry.get("original_price") is not None else None),
        "description":    entry.get("description"),
        "features":       features,
        "in_stock":       1 if entry.get("in_stock", True) else 0,
        "badge":          entry.get("badge"),
        "image":          _img(entry.get("image")),
    }
